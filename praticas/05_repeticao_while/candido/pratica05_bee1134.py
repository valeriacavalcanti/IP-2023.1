ca = 0
cg = 0
cd = 0
while True:
    cod = int(input())
    if cod == 1:
        ca = ca + 1
    elif cod == 2:
        cg = cg + 1
    elif cod == 3:
        cd = cd + 1
    elif cod == 4:
        break
print('MUITO OBRIGADO')
print('Alcool:',ca)
print('Gasolina:',cg)
print('Diesel:',cd)
    
    
    
        
